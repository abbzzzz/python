# modulus - This is the remainder of a number when it is subdivided by another one
number9 = 14
number10 = 5
modulus = number9 % number10
print("The modulus is: ", modulus)

# Floor Division: This is the number of times a number subdivides another one ignoring the remainder
floor = number9 // number10
print("The floor division is: ", floor)

# Exponent
# The power of a number
number11 = 10
exponent = number11 ** 2
print("the exponent is: ", exponent)