# Python data types
# String: a series of characters that are inside of quotes
name = "Kayden Break"
print(name)
print(type(name))

# Numeric
# Int - indicates a whole number in python. For an integer we don't use quotes
age = 39
print("The age of a person is: ", age)
print(type(age))

# Float - this is a number with a decimal
coordinates = 15.4524
print("The coordinates are: ", coordinates)
print(type(coordinates))